    private void tabelaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelaKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_DOWN || evt.getKeyCode()==KeyEvent.VK_UP){
             try{
            int row = tabela.getSelectedRow();
            String tabela_click = (tabela.getModel().getValueAt(row, 0).toString());
            
            String sql = "select * from empregados where id='"+tabela_click+"' ";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                int id = rs.getInt("id");
                campoId.setText(String.valueOf(id));
                String nome = rs.getString("nome");
                campoNome.setText(nome);
                String sob = rs.getString("sobrenome");
                campoSobrenome.setText(sob);
                String funcao = rs.getString("funcao");
                campoFunc.setText(funcao);
                int idade = rs.getInt("idade");
                campoIdade.setText(String.valueOf(idade));
                
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_tabelaKeyPressed

